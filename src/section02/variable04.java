package section02;
/*
 * 실수형
 * 	소수부나 지수부가 있는 수를 가르킨다
 * 	실수를 저장할수 있는 자료형
 * 	
 * 	float / 소수점 7자리까지 표현 가능
 * 	double / 소수점 15자리 까지 표현 가능
 * 
 */
public class variable04 {
	public static void main(String[] args) {
		float iFloat = 10.1f;
		double iDouble = 10.1d;
		double iDouble2 = 10.1; //double형 d생략 가능
		
		System.out.println(iFloat);
		System.out.println(iDouble);
		System.out.println(iDouble2);
		
		
	}
		
}

  